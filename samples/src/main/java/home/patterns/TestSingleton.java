package home.patterns;

import java.io.FileInputStream;
import java.io.FileNotFoundException;
import java.io.FileOutputStream;
import java.io.IOException;
import java.io.ObjectInputStream;
import java.io.ObjectOutputStream;

/**
 * @author sahoos2
 * 
 */
public class TestSingleton {

	public static void main(String[] args) {

		Singleton singleton = Singleton.getInstance();

		store(singleton, "Singleton.ser");

		Singleton singleton2 = (Singleton) load("Singleton.ser");

		System.out.println(singleton);
		System.out.println(singleton2);

		singleton.count = 5;

		System.out.println(singleton.count);
		System.out.println(singleton2.count);
	}

	private static void store(Object obj, String fileName) {
		try {
			FileOutputStream fileOut = new FileOutputStream(fileName);
			ObjectOutputStream out = new ObjectOutputStream(fileOut);
			out.writeObject(obj);
			out.close();
			fileOut.close();
		} catch (FileNotFoundException e) {
			e.printStackTrace();
		} catch (IOException e) {
			e.printStackTrace();
		}
	}

	private static Object load(String fileName) {
		Object obj = null;
		try {
			FileInputStream fileIn = new FileInputStream(fileName);
			ObjectInputStream in = new ObjectInputStream(fileIn);
			obj = in.readObject();
			in.close();
			fileIn.close();
		} catch (FileNotFoundException e) {
			e.printStackTrace();
		} catch (IOException e) {
			e.printStackTrace();
		} catch (ClassNotFoundException e) {
			e.printStackTrace();
		}
		return obj;
	}

}
